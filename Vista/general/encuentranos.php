const Toast = Swal.mixin({
    toast: true,
    position: 'top',
    customClass: {
      popup: 'colored-toast',
    },
    showConfirmButton: false,
    timer: 3500,
    timerProgressBar: true,
  })
  
    function enviar() {

      // Obtener los valores de los campos
      const nombre = document.getElementById('nombre').value;
      const dudas = document.getElementById('dudas').value;

      // Validar campos
      if (!nombre || !dudas) {
          Toast.fire({
            icon: 'error',
            title: 'Por favor, completa todos los campos.',
          });
    
          return;
      }

      // Crear el mensaje para WhatsApp
      const mensaje = `Hola, soy ${nombre}. 
      
      Mis dudas e inquietudes son: 
      
      ${dudas}`;
      const numeroWhatsApp = '573144432212'; // Cambia este número por el número de WhatsApp deseado
      const urlWhatsApp = `https://wa.me/${numeroWhatsApp}?text=${encodeURIComponent(mensaje)}`;

      document.getElementById('nombre').value = '';
      document.getElementById('dudas').value = '';

      // Redirigir a WhatsApp
      window.open(urlWhatsApp, '_blank');

      Toast.fire({
        icon: 'success',
        title: 'Solicitud enviada',
      });
  }
